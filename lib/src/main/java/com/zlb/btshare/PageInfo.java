package com.zlb.btshare;

/**
 * Created by zhanglibin on 2014/11/8.
 * 详情页名称和地址
 */
public class PageInfo {
    /**
     * 详情页名称
     */

    private String name;
    /**
     * 详情页地址
     */
    private String url;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }


}
